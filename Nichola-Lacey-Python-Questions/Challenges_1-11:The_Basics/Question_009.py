# Write a program that will ask for a number of days and then will show how many hours, minutes and seconds are in that number of days.

no_of_days = int(input("Enter the number of days > "))

print(f"You entered the {no_of_days} days in which you have {no_of_days*24} hours , {no_of_days*24*60} minutes and {no_of_days*24*60*60} seconds.")